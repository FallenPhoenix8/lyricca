type LogoFullProps = {
  className?: string
  /** Total time for the pen to travel across the word. */
  writeDuration?: number
  /** Time to leave the finished word visible before it clears. */
  holdDuration?: number
  /** Time used to fade the word away before the mask resets. */
  clearDuration?: number
  isRepeating?: boolean
  startDelay?: number
}

gsap.registerPlugin(useGSAP)

export function LogoFull({
  className,
  writeDuration = m3ExpressiveDuration.spatial.slow.seconds * 2,
  holdDuration = 0,
  clearDuration = m3ExpressiveDuration.effect.fast.seconds,
  isRepeating = false,
  startDelay = 0,
}: LogoFullProps) {
  useM3Motion()

  const logoRef = useRef<SVGSVGElement>(null)
  const reactId = useId()
  const cleanId = reactId.replace(/:/g, "")
  const writeMaskId = `logo-write-mask-${cleanId}`

  useGSAP(
    () => {
      const q = gsap.utils.selector(logoRef)
      const stroke = q<SVGPathElement>(".logo-write-stroke")[0]
      const dot = q<SVGCircleElement>(".logo-write-dot")[0]
      const fill = q<SVGGElement>(".logo-written-fill")[0]

      if (!stroke || !dot || !fill) return

      const length = stroke.getTotalLength()

      gsap.set(stroke, {
        strokeDasharray: length,
        strokeDashoffset: length,
      })

      gsap.set(dot, {
        opacity: 0,
        scale: 0,
        transformOrigin: "50% 50%",
        transformBox: "fill-box",
      })

      gsap.set(fill, { opacity: 1 })

      const tl = gsap.timeline({
        repeat: isRepeating ? -1 : 0,
        delay: Math.max(0, startDelay),
        defaults: { ease: "none" },
      })

      tl.to(stroke, {
        strokeDashoffset: 0,
        duration: writeDuration,
      })

      // The i-dot is not part of the continuous pen path, so reveal it with a tiny tap.
      tl.to(dot, {
        opacity: 1,
        scale: 1,
        duration: 0.12,
        ease: "none",
      })

      if (holdDuration > 0) {
        tl.to({}, { duration: holdDuration })
      }

      if (isRepeating) {
        // Fade the visible logo out first. Only after it is invisible do we reset
        // the mask stroke. Otherwise the repeat boundary creates a visible snap.
        tl.to(fill, {
          opacity: 0,
          duration: clearDuration,
          ease: "power1.inOut",
        })

        // Reset everything to the initial hidden-mask state for the next loop.
        // Dot must reset to 0 here, not 1; otherwise it appears at the start
        // of the next cycle and looks like a jump.
        tl.set(stroke, { strokeDashoffset: length })
        tl.set(dot, { opacity: 0, scale: 0 })
        tl.set(fill, { opacity: 1 })
      }

      return () => tl.kill()
    },
    {
      scope: logoRef,
      dependencies: [
        writeDuration,
        holdDuration,
        clearDuration,
        isRepeating,
        startDelay,
      ],
      revertOnUpdate: true,
    },
  )

  return (
    <svg
      ref={logoRef}
      viewBox="0 0 154 55"
      xmlns="http://www.w3.org/2000/svg"
      className={className}
      aria-hidden="true"
      fill="none"
    >
      <defs>
        <mask
          id={writeMaskId}
          x="-8"
          y="-8"
          width="170"
          height="72"
          maskUnits="userSpaceOnUse"
          style={{ maskType: "alpha" }}
        >
          <path
            className="logo-write-stroke"
            d={WRITE_PATH}
            fill="none"
            stroke="white"
            strokeWidth="19"
            strokeLinecap="round"
            strokeLinejoin="round"
          />

          <circle
            className="logo-write-dot"
            cx="78.03"
            cy="2.95"
            r="5.2"
            fill="white"
          />
        </mask>
      </defs>

      <g
        className="logo-written-fill"
        fill="var(--primary)"
        mask={`url(#${writeMaskId})`}
      >
        {LOGO_PATHS.map((d, index) => (
          <path key={`fill-${index}`} d={d} />
        ))}
      </g>
    </svg>
  )
}
